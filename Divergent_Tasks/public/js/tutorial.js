//bombilla de crear tablero
function MensajedeAyuda() {
    var mensajedeayuda = document.getElementsByClassName("mensaje-de-ayuda")[0];
    if(mensajedeayuda.style.display == "block"){
        mensajedeayuda.style.display = "none";
    }
    else{   mensajedeayuda.style.display = "block"};
}

